export function checkAccess (auth) 
{
    if (auth && auth.role_id == 1)
        return true;

    if (!auth)
        return false;

    if (!auth.business)
        return false;
    
    if (!auth.business.subscription)
        return false;
    
    if (auth.business.subscription.is_expired)
        return false;

    return true;
}

